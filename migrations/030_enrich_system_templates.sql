-- 1. 快速上手（原"标准教程"）：标准职业技能培训，理论和实操均衡
UPDATE course_templates SET content = '【课程整体结构】
课程分为三个阶段：基础认知（foundation）→ 工具实践（practice）→ 综合应用（assessment）。每阶段3-5章，难度线性递增。

【每章结构要求】
1. scene_hook（场景导入）：100字以内，以"你"开头描述真实职场情境
2. skim_summary（速览要点）：3条要点，分号分隔，每条不超过20字
3. full_content（正文）：600-900字，结构为"概念解释 → 原理/机制 → 操作步骤 → 实际示例"。术语首次出现用【术语名】标注。插入1-2个 <!--CHECKPOINT:问题|解析提示--> 标记
4. code_example（代码示例）：完整可运行示例，三个反引号围栏包裹并标注语言。不涉及编程的章节填空字符串
5. misconception_block（误区纠正）：以"⚠ 很多人误认为…实际上…"格式纠正1个常见误解
6. prereq_adaptive（自适应扩展）：100字以内，补充进阶细节或边界案例

【代码格式规则】
- 代码块使用三个反引号围栏（```python / ```sql / ```bash 等）
- 行内代码用单个反引号包裹，如 `nginx -t`
- 禁止在代码围栏内写伪代码或纯中文描述

【教学风格】
语言正式专业但不生硬。理论讲解与实操案例并重，每引入一个概念都要跟随一个"为什么"的解释。面向高等教育或企业培训场景，适合系统性学习的职业人士。'
WHERE name = '快速上手' AND is_system = TRUE;

-- 2. 实操导向：以项目驱动，边做边学，减少理论推导
UPDATE course_templates SET content = '【课程整体结构】
课程分为三个阶段：工具入门（setup）→ 项目实战（projects）→ 故障排查（troubleshooting）。每阶段3-6章，以真实工作任务为主线。

【每章结构要求】
1. scene_hook（任务场景）：80字以内，以"你的主管要求你…"或"你遇到了…问题"开头，给出明确任务目标
2. skim_summary（速览要点）：3条可操作步骤，分号分隔，每条不超过20字
3. full_content（正文）：500-800字，结构为"任务目标 → 操作步骤（分步编号）→ 预期结果 → 验证方法"。每一步必须有具体的命令或操作。避免长篇原理阐述，必要时用"为什么这一步重要…"一句话交代
4. code_example（代码示例）：提供1个可直接复制运行的完整示例。如果有配置文件，一并给出。用三个反引号围栏包裹并标注语言类型
5. misconception_block（常见踩坑）：以"⚠ 新手常犯错误…正确做法…"格式，指出1个本任务最常见的操作失误及正确方法
6. prereq_adaptive（进阶挑战）：为完成本节任务的学员提供一个扩展练习或更复杂变体，80字以内

【代码格式规则】
- 代码块使用三个反引号围栏（```python / ```sql / ```bash / ```yaml 等）
- 行内命令用单个反引号包裹，如 `docker-compose up -d`
- 禁止在代码围栏内写伪代码

【教学风格】
以"做"为主线，而非"讲"。语言简洁直接，用"你"称呼学员。每个概念都紧跟一个动手练习。适合中职、高职、企业技能培训或希望快速上手的在职人员。'
WHERE name = '实操导向' AND is_system = TRUE;

