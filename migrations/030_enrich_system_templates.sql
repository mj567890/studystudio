【每章结构要求】
1. scene_hook（思维引子）：80字以内，提出一个引人深思的问题或反直觉现象，激发学员思考"为什么会这样"
2. skim_summary（核心命题）：3条本课要回答的关键问题，分号分隔，每条不超过20字
3. full_content（正文）：700-1000字，结构为"核心概念定义 → 底层原理/数学模型（如适用）→ 与其他概念的关联 → 实际中如何体现 → 常见变体或例外"。术语首次出现用【术语名】标注，重要术语需给出中英文对照。插入1-2个<!--CHECKPOINT:思考题|参考思路--> 标记
4. code_example（代码示例）：提供1个验证或演示原理的最小示例（而非完整应用），用三个反引号围栏包裹。不涉及编程的章节可用逻辑流程图或对比表格代替
5. misconception_block（概念澄清）：以"⚠ 很多人将【A】与【B】混淆…实际上它们的本质区别是…"格式，纠正1个概念层面的常见误解
6. prereq_adaptive（延伸阅读）：补充相关学术文献方向、工业界实践差异或前沿发展动态，100字以内

【代码格式规则】
- 代码块使用三个反引号围栏，标注语言类型
- 行内术语/代码用单个反引号包裹
- 禁止在代码围栏内写伪代码

【教学风格】
严谨、透彻，追求"知其所以然"。允许适当引入学术概念和理论模型，但始终要落回到实际应用中的体现。适合考研备考、技术研究者或希望深入理解底层原理的资深从业者。'
WHERE name = '理论基础' AND is_system = TRUE;

-- 4. 速成精简：直奔结论，清单式表达，最少的阅读时间
UPDATE course_templates SET content = '【课程整体结构】
课程分为三个阶段：必知概念（must-know）→ 必会操作（must-do）→ 避坑清单（pitfalls）。每阶段2-4章，每章控制在400字以内。

【每章结构要求】
1. scene_hook（一句定位）：30字以内，一句话说明"你要解决什么问题"
2. skim_summary（三步搞定）：3条分步骤操作，每条不超过15字，用"→"连接形成操作链
3. full_content（正文）：300-500字，采用"结论先行"风格——先给结论再给简要理由。使用分点列表而非段落叙述。每个要点不超过3行
4. code_example（代码示例）：只给最核心的代码片段或命令，用三个反引号围栏包裹。如有多种实现方式，只给最推荐的一种
5. misconception_block（一错就废）：以"⚠ 别…"开头，用一句话指出最致命的错误，再用一句话给正确做法
6. prereq_adaptive（下一步）：一句话推荐下一阶段学习方向，40字以内

【代码格式规则】
- 代码块使用三个反引号围栏，标注语言类型
- 行内命令用单个反引号包裹
- 禁止写伪代码或占位符

【教学风格】
极简主义。去掉所有铺垫、修饰和"为什么"，只留"是什么"和"怎么做"。用短句、用列表、用加粗。适合考前突击、快速入门或需要迅速查阅的实战者。'
WHERE name = '速成精简' AND is_system = TRUE;

